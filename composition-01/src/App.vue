<template>
  <section class="container">
    <h2>{{ user.name }}</h2>
    <p>{{ user.age }}</p>
    <button @click="setAge">Change Age</button>
  </section>
</template>

<script>
// import { ref } from 'vue';
import { reactive } from 'vue';
export default {
  setup() {
    // const username = ref('Muhammad Aziz Al-assad');
    // const age = ref(21);
    const user = reactive({
      name: 'Muhammad Aziz Al-assad',
      age: 21
    });

    const setAge = () => {
      user.age = 22;
    };

    return { user, setAge };
  }
};
</script>

